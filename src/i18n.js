/** 当前语言（默认中文） */
let currentLang = 'zh';

/** 读取当前语言代码 */
export function getLang() {
  return currentLang;
}

/** 设置当前语言（非法值忽略），返回最终语言 */
export function setLang(code) {
  if (DICT[code]) currentLang = code;
  return currentLang;
}

/** 取某语言的元信息 */
export function langMeta(code) {
  return LANGS.find((l) => l.code === code) || LANGS[0];
}

/**
 * 取指定 key 的当前语言文案；缺失时回退中文，再回退英文，最后回退 key。
 * 支持 {min} 等占位符替换（通过第二参 vars）。
 * @param {string} key
 * @param {Record<string,string|number>} [vars]
 */
export function getText(key, vars) {
  const table = DICT[currentLang] || DICT.zh;
  let val = key in table ? table[key] : key in DICT.zh ? DICT.zh[key] : key in DICT.en ? DICT.en[key] : key;
  if (vars) {
    Object.keys(vars).forEach((k) => {
      val = val.replace(new RegExp('\\{' + k + '\\}', 'g'), String(vars[k]));
    });
  }
  return val;
}

/**
 * 从一个多语对象中取当前语言值（带回退链：当前 -> zh -> en -> 任意）。
 * 兼容历史 {zh,en} 与 v2 的 name_i18n / desc_i18n。
 * @param {Record<string,string>|string} field
 */
export function pick(field) {
  if (field == null) return '';
  if (typeof field === 'string') return field;
  if (field[currentLang]) return field[currentLang];
  if (field.zh) return field.zh;
  if (field.en) return field.en;
  const vals = Object.values(field).filter(Boolean);
  return vals.length ? vals[0] : '';
}
